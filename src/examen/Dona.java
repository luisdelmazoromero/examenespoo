public class Dona 
{
    private String chocolate;
    private String azucar;
    private String rompope;
    private String fresa;
    private String moka;

    public void setChocolate(String chocolate) {
        this.chocolate = chocolate;
    }

    public void setAzucar(String azucar) {
        this.azucar = azucar;
    }

    public void setRompope(String rompope) {
        this.rompope = rompope;
    }

    public void setFresa(String fresa) {
        this.fresa = fresa;
    }

    public void setMoka(String moka) {
        this.moka = moka;
    }

    public String getChocolate() {
        return chocolate;
    }

    public String getAzucar() {
        return azucar;
    }

    public String getRompope() {
        return rompope;
    }

    public String getFresa() {
        return fresa;
    }

    public String getMoka() {
        return moka;
    }
}
